<h2>WHAT'S COVERED HERE?</h2>
<p>This book covers collisions between points, circles, rectangles, lines, polygons, and triangles. These examples are meant to be as readable and easily understood as possible. There are definitely faster, more efficient ways to detect these collisions, but this book is intended to be friendly and teach the principles with minimal math.</p>

<p>Each section include a description of the collision algorithm and an interactive example built using <code>processing.js</code>. You can <a href="https://github.com/jeffThompson/CollisionDetection">view the source code for all the examples (and this book!) on GitHub.</a></p>

<p class="callout"><strong>NOTE!</strong> If you're on a mobile device, the examples might not work super well for you. They are designed for mouse input, so if you're getting frustrated or your finger is in the way, try the site on a computer.</p>

<h2>WHAT'S NOT?</h2>
<p>As with any book, there's a lot more useful material than could be covered here. Things that aren't discussed are mostly left out because the math gets too complicated. Three-dimensional space isn't touched on. Ellipses, which seem like they should be pretty easy, are actually very difficult.</p>

<p>If there's a specific collision not covered that would be helpful, please please <a href="https://github.com/jeffThompson/CollisionDetection/issues"> submit an issue with a request</a> or, better yet, submit a working example that you've built!</p>

<h2>ISSUES?</h2>
